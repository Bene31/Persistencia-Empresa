    private void btnovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnovoActionPerformed
        servicocontroller.setServicoDigitado(null);
        servicocontroller.setServicoSelecionado(null);
        jTable1.clearSelection();
        servicocontroller.novo();
        txtempresa.setText("");
    }//GEN-LAST:event_btnovoActionPerformed

    private void btpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btpesquisarActionPerformed
        try {
            Empresa e = new Empresa();
            
            if (!txtempresa.getText().replace(".", "").replace("/", "").replace("-", "").trim().equals(""))
                e.setCnpj(txtempresa.getText());
            
            Servico s = new Servico();
            s.setTitulo(txttitulo.getText());
            s.setEmpresa(e);
            
            servicocontroller.setServicoDigitado(s);
            servicocontroller.pesquisar();
        } catch (RemoteException e) {
            JOptionPane.showMessageDialog(this,
                    "Erro " + e.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btpesquisarActionPerformed

    private void btexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btexcluirActionPerformed
        try {
            if (servicocontroller.getServicoSelecionado() == null) {
                throw new Exception("Selecione o Serviço que deseja excluir!");
            }

            if (JOptionPane.showConfirmDialog(this,
                    "Deseja realmente excluir o serviço?",
                    "Excluir serviço",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {
                    servicocontroller.excluir();
                    this.btnovoActionPerformed(evt);
                    JOptionPane.showMessageDialog(this,
                            "Serviço excluído com sucesso",
                            "Excluir serviço",
                            JOptionPane.INFORMATION_MESSAGE);
                } catch (RemoteException e) {
                    JOptionPane.showMessageDialog(this,
                            "Erro " + e.getMessage(),
                            "Erro",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btexcluirActionPerformed

