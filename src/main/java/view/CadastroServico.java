    private void btsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsalvarActionPerformed
        try {
            Empresa e = new Empresa();
            e.setCnpj(txtempresa.getText());
            
            Servico s = new Servico();
            s.setCodigo(Integer.parseInt(txtcodigo.getText()));
            s.setTitulo(txttitulo.getText());
            
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yy");
            
            if (!txtDataRealizacao.getText().equals(""))
                s.setDataRealizacao(format.parse(txtDataRealizacao.getText()));
            
            if (!txtvalor.getText().equals(""))
                s.setValor(Float.parseFloat(txtvalor.getText().replace(",", ".")));
            
            s.setEmpresa(e);
            
            servicocontroller.setServicoDigitado(s);
            servicocontroller.salvar();
            this.btnovoActionPerformed(evt);
            JOptionPane.showMessageDialog(this, 
                "Serviço salvo com sucesso",
                "Salvar serviço",
                JOptionPane.INFORMATION_MESSAGE);              
        } catch(EntityNotFoundException e) {
            JOptionPane.showMessageDialog(this, 
                "Empresa inexistente!",
                "Falha de Validação",
                JOptionPane.WARNING_MESSAGE);
        } catch(IllegalStateException e) {
            JOptionPane.showMessageDialog(this, 
                "Empresa inexistente!",
                "Falha de Validação",
                JOptionPane.WARNING_MESSAGE);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, 
                e.getMessage(),
                "Falha de Validação",
                JOptionPane.WARNING_MESSAGE);              
        } 
    }//GEN-LAST:event_btsalvarActionPerformed

